    <h2 id="mi-a-vizkő">Mi a vízkő és miért káros?</h2>
    <p>A vízkő (mészkő-lerakódás) a vízben lévő kalcium- és magnéziumsók lerakódása. Minden vízforralással egy vékony réteg képződik a fűtőszálon. Mivel a vízkő kiváló hőszigetelő, a fűtőszálnak egyre több energiát kell felhasználnia a víz felmelegítéséhez.</p>
    <ul>
      <li><strong>3 mm vízkő:</strong> 25%-kal több áramot fogyaszt</li>
      <li><strong>6 mm vízkő:</strong> 40%-kal több áramot, a fűtőszál átéghet</li>
      <li><strong>10 mm felett:</strong> Szinte biztos a fűtőszál tönkremenetele</li>
    </ul>

    <h2 id="jelek">Jelek, hogy ideje vízkőteleníteni</h2>
    <ul>
      <li>🔊 <strong>Kattogó, bugyborékoló vagy sistergő hang</strong> a melegítés során</li>
      <li>🐌 <strong>Lassabb melegítés</strong> – korábban 1 óra alatt melegedett fel, most 2 kellett</li>
      <li>⚡ <strong>Magasabb villanyszámla</strong> indok nélkül</li>
      <li>💧 <strong>Hamarabb elfogy a meleg víz</strong> ugyanannyi fogyasztás mellett</li>
      <li>🌡️ <strong>Nem éri el a beállított hőmérsékletet</strong></li>
    </ul>

